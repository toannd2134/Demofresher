import UIKit
extension UIColor {
    class func  greenColor() -> UIColor {
        return UIColor(red: 0.22, green: 0.59, blue: 0.42, alpha: 1.00)
        
    }
    class func  darkGreenColor() -> UIColor {
        return UIColor(red: 0.11, green: 0.22, blue: 0.19, alpha: 1.00)
    }
    class func  lightGreenColor() -> UIColor {
        return UIColor(red: 0.05, green: 0.67, blue: 0.51, alpha: 1.00)
    }
}
